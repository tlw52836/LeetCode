package OJ练习;

import java.util.Scanner;

public class 自测本地通过提交为0 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        while (in.hasNext()) {
            long a = in.nextLong();
            long b = in.nextLong();
            System.out.println(a+b);
        }
    }
}
